package Util;

public class ItemRatioRow
{
	private String name;
	private int id;
	private int placed;
	private int broke;
	private long lastReported;
	
	public ItemRatioRow(String name, int id, int placed, int broke, long lastReported ) {
		this.name = name;
		this.id = id;
		this.placed = placed;
		this.broke = broke;
		this.lastReported= lastReported;
	}

	private String getName()
	{
		return name;
	}

	private void setName(String name)
	{
		this.name = name;
	}

	private int getId()
	{
		return id;
	}

	private void setId(int id)
	{
		this.id = id;
	}

	private int getPlaced()
	{
		return placed;
	}

	private void setPlaced(int placed)
	{
		this.placed = placed;
	}

	private int getBroke()
	{
		return broke;
	}

	private void setBroke(int broke)
	{
		this.broke = broke;
	}

	private long getLastReported()
	{
		return lastReported;
	}

	private void setLastReported(long lastReported)
	{
		this.lastReported = lastReported;
	}
	
	
	

}
